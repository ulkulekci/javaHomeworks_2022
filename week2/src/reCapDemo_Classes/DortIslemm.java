//Classler ile çalışmak

public class DortIslemm {
	
	public int topla(int sayi1,int sayi2) {
		return sayi1+sayi2;
	}
	
	public int cıkar(int sayi1,int sayi2) {
		return sayi1-sayi2;
	}
	
	public int carp(int sayi1,int sayi2) {
		return sayi1*sayi2;
	}
	
	public int bol(int sayi1,int sayi2) {
		return sayi1/sayi2;
	}
}